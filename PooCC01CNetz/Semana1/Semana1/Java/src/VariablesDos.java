public class VariablesDos {
    public static void main(String[] args) {
        //Variables de tipo caracter
        char cvar = 'h';
        System.out.println("Valor del char: " + cvar);
        //Vraiables de tipo logico
        boolean b1var = false;
        boolean b2var = true;
        System.out.println("Valor del logico: " + b1var);
        System.out.println("Valor del logico: " + b2var);
    }
}
