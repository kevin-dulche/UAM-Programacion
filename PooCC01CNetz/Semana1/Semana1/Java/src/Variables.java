public class Variables {
    public static void main(String[] args) {
        //Variables de tipo entero
        byte bvar = 127;
        short svar = 12;
        int ivar = 4;
        long lvar = 10;
        System.out.println("Valor del byte: " + bvar);
        System.out.println("Valor del short: " + svar);
        System.out.println("Valor del int: " + ivar);
        System.out.println("Valor del long: " + lvar);
        //Variables de tipo flotante
        float f1var = 12.45f;
        float f2var = (float) 12.45;
        double dvar = 24.57;
        System.out.println("Valor del float: " + f1var);
        System.out.println("Valor del float: " + f2var);
        System.out.println("Valor del double: " + dvar);
    }
}
