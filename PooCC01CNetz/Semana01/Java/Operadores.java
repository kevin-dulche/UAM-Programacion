public class Operadores {
    public static void main(String[] args) {
        // Operadores aritmeticos
        // + - * / %
        // Operadores relacionales
        // > >= < <= == !=
        boolean res = 4 < 0;
        System.out.println("El resultado es: " + res);
        // Operadores logicos
        // && || !
        res = true && false;
        System.out.println(res);
        res = false || true;
        System.out.println(res);
    }
}
